import argparse
import csv
import json
import math
from pathlib import Path

import numpy as np


WRIST_IDX = 0
INDEX_MCP_IDX = 5
PINKY_MCP_IDX = 17
MIDDLE_MCP_IDX = 9


def parse_args():
    parser = argparse.ArgumentParser(description="Extract wrist trajectory from MediaPipe landmark JSON.")
    parser.add_argument("--input", required=True, help="Path to landmarks.json generated by extract_hand_landmarks.py")
    parser.add_argument("--output-dir", required=True, help="Directory where processed trajectory files will be written")
    parser.add_argument("--smoothing-window", type=int, default=5, help="Odd window size for moving-average smoothing")
    return parser.parse_args()


def moving_average(values, window_size):
    if window_size <= 1:
        return values[:]
    half = window_size // 2
    smoothed = []
    for index in range(len(values)):
        start = max(0, index - half)
        end = min(len(values), index + half + 1)
        segment = values[start:end]
        smoothed.append(sum(segment) / len(segment))
    return smoothed


def angle_2d(point_a, point_b):
    dx = point_b[0] - point_a[0]
    dy = point_b[1] - point_a[1]
    return math.atan2(dy, dx)


def vec3(landmark):
    return np.array([landmark["x"], landmark["y"], landmark["z"]], dtype=np.float64)


def normalize(vector):
    norm = np.linalg.norm(vector)
    if norm < 1e-9:
        return vector * 0.0
    return vector / norm


def main():
    args = parse_args()
    input_path = Path(args.input)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    with open(input_path, "r", encoding="utf-8") as f:
        payload = json.load(f)

    fps = payload["metadata"]["fps"]
    frames = payload["frames"]

    trajectory = []

    for frame in frames:
        hands = frame.get("hands", [])
        if not hands:
            trajectory.append(
                {
                    "frame_index": frame["frame_index"],
                    "time_s": frame["frame_index"] / fps,
                    "detected": False,
                    "wrist_x": None,
                    "wrist_y": None,
                    "wrist_z": None,
                    "palm_axis_angle_rad": None,
                    "pour_angle_rad": None,
                }
            )
            continue

        landmarks = hands[0]["landmarks"]
        wrist = landmarks[WRIST_IDX]
        index_mcp = landmarks[INDEX_MCP_IDX]
        pinky_mcp = landmarks[PINKY_MCP_IDX]
        middle_mcp = landmarks[MIDDLE_MCP_IDX]

        wrist_v = vec3(wrist)
        index_v = vec3(index_mcp)
        pinky_v = vec3(pinky_mcp)
        middle_v = vec3(middle_mcp)

        palm_axis_angle = angle_2d((pinky_mcp["x"], pinky_mcp["y"]), (index_mcp["x"], index_mcp["y"]))
        pour_angle = angle_2d((wrist["x"], wrist["y"]), (middle_mcp["x"], middle_mcp["y"]))
        palm_axis_vec = normalize(index_v - pinky_v)
        wrist_to_middle_vec = normalize(middle_v - wrist_v)
        palm_normal_vec = normalize(np.cross(index_v - wrist_v, pinky_v - wrist_v))

        trajectory.append(
            {
                "frame_index": frame["frame_index"],
                "time_s": frame["frame_index"] / fps,
                "detected": True,
                "wrist_x": wrist["x"],
                "wrist_y": wrist["y"],
                "wrist_z": wrist["z"],
                "palm_axis_angle_rad": palm_axis_angle,
                "pour_angle_rad": pour_angle,
                "palm_axis_x": float(palm_axis_vec[0]),
                "palm_axis_y": float(palm_axis_vec[1]),
                "palm_axis_z": float(palm_axis_vec[2]),
                "wrist_to_middle_x": float(wrist_to_middle_vec[0]),
                "wrist_to_middle_y": float(wrist_to_middle_vec[1]),
                "wrist_to_middle_z": float(wrist_to_middle_vec[2]),
                "palm_normal_x": float(palm_normal_vec[0]),
                "palm_normal_y": float(palm_normal_vec[1]),
                "palm_normal_z": float(palm_normal_vec[2]),
            }
        )

    detected_rows = [row for row in trajectory if row["detected"]]
    if detected_rows:
        xs = [row["wrist_x"] for row in detected_rows]
        ys = [row["wrist_y"] for row in detected_rows]
        zs = [row["wrist_z"] for row in detected_rows]
        palm_angles = [row["palm_axis_angle_rad"] for row in detected_rows]
        pour_angles = [row["pour_angle_rad"] for row in detected_rows]
        palm_axis_x = [row["palm_axis_x"] for row in detected_rows]
        palm_axis_y = [row["palm_axis_y"] for row in detected_rows]
        palm_axis_z = [row["palm_axis_z"] for row in detected_rows]
        palm_normal_x = [row["palm_normal_x"] for row in detected_rows]
        palm_normal_y = [row["palm_normal_y"] for row in detected_rows]
        palm_normal_z = [row["palm_normal_z"] for row in detected_rows]

        xs_s = moving_average(xs, args.smoothing_window)
        ys_s = moving_average(ys, args.smoothing_window)
        zs_s = moving_average(zs, args.smoothing_window)
        palm_s = moving_average(palm_angles, args.smoothing_window)
        pour_s = moving_average(pour_angles, args.smoothing_window)
        palm_axis_x_s = moving_average(palm_axis_x, args.smoothing_window)
        palm_axis_y_s = moving_average(palm_axis_y, args.smoothing_window)
        palm_axis_z_s = moving_average(palm_axis_z, args.smoothing_window)
        palm_normal_x_s = moving_average(palm_normal_x, args.smoothing_window)
        palm_normal_y_s = moving_average(palm_normal_y, args.smoothing_window)
        palm_normal_z_s = moving_average(palm_normal_z, args.smoothing_window)

        for row, x_s, y_s, z_s, palm_angle_s, pour_angle_s, pax_s, pay_s, paz_s, pnx_s, pny_s, pnz_s in zip(
            detected_rows,
            xs_s,
            ys_s,
            zs_s,
            palm_s,
            pour_s,
            palm_axis_x_s,
            palm_axis_y_s,
            palm_axis_z_s,
            palm_normal_x_s,
            palm_normal_y_s,
            palm_normal_z_s,
        ):
            row["wrist_x_smooth"] = x_s
            row["wrist_y_smooth"] = y_s
            row["wrist_z_smooth"] = z_s
            row["palm_axis_angle_rad_smooth"] = palm_angle_s
            row["pour_angle_rad_smooth"] = pour_angle_s
            row["palm_axis_x_smooth"] = pax_s
            row["palm_axis_y_smooth"] = pay_s
            row["palm_axis_z_smooth"] = paz_s
            row["palm_normal_x_smooth"] = pnx_s
            row["palm_normal_y_smooth"] = pny_s
            row["palm_normal_z_smooth"] = pnz_s

    for row in trajectory:
        if not row["detected"]:
            row["wrist_x_smooth"] = None
            row["wrist_y_smooth"] = None
            row["wrist_z_smooth"] = None
            row["palm_axis_angle_rad_smooth"] = None
            row["pour_angle_rad_smooth"] = None
            row["palm_axis_x_smooth"] = None
            row["palm_axis_y_smooth"] = None
            row["palm_axis_z_smooth"] = None
            row["palm_normal_x_smooth"] = None
            row["palm_normal_y_smooth"] = None
            row["palm_normal_z_smooth"] = None

    json_output = output_dir / "wrist_trajectory.json"
    with open(json_output, "w", encoding="utf-8") as f:
        json.dump({"metadata": payload["metadata"], "trajectory": trajectory}, f, indent=2)

    csv_output = output_dir / "wrist_trajectory.csv"
    fieldnames = list(trajectory[0].keys()) if trajectory else []
    with open(csv_output, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(trajectory)

    print(f"Frames processed: {len(trajectory)}")
    print(f"Detected frames: {sum(1 for row in trajectory if row['detected'])}")
    print(f"Wrist JSON: {json_output}")
    print(f"Wrist CSV: {csv_output}")


if __name__ == "__main__":
    main()